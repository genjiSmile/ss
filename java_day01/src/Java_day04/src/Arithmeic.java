public class Arithmeic {
    public static void main(String[] args){
        int a=10;
        int b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println((double)a/b);//  0.5
        System.out.println(a%b);
        System.out.println("---------------");

        System.out.println("a++"+ a++);

        System.out.println("a="+a);

        System.out.println("++a"+ ++a);

        System.out.println("b--"+ b--);

        System.out.println("--b"+ --b);

    }

}
